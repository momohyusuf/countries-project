// find a single country with its name
searchCountryBtn.addEventListener("click", searchForCountryByName);
function searchForCountryByName() {
  countriesToDisplay = countriesData.filter((item) =>
    item.name
      .toLocaleLowerCase()
      .includes(searchCountryValue.value.toLowerCase().trim())
  );

  //   if countries array is empty return a message to a user
  if (countriesToDisplay.length === 0) {
    return (allCountiresConatianer.innerHTML = `<div class=text-center><h1>Sorry country not found 💔</h1>
     <p>Try searching for a different value</p></div>
     `);
  }
  return (allCountiresConatianer.innerHTML = countriesToDisplay
    .map((item) => countryCardSmall(item))
    .join(""));
}
