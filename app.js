const allCountiresConatianer = document.getElementById(
  "all--countries--container"
);
const searchCountryValue = document.getElementById("country--search--value");
const searchCountryBtn = document.getElementById("search--country--btn");
const singleCountryModal = document.getElementById("single--country--modal");

let countriesToDisplay = [];

// a function to display all countries
function displayAllCountries() {
  countriesToDisplay = countriesData.map((item) => countryCardSmall(item));
  return (allCountiresConatianer.innerHTML = countriesToDisplay.join(""));
}
displayAllCountries();
